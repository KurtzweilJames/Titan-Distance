<div class="container">
    <p>Our cross country course is run on nearly 100% grass around the athletic fields of Glenbrook South. Over the
        years, tens of thousands of runners have crossed the finish line of our course in meets ranging from Duals to
        IHSA Competitions. The course is mostly-flat, and cut short for most meets.</p>
</div>
